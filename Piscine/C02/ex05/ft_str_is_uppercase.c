int	check(char a)
{
	if ((a >= 'A') && (a <= 'Z'))
		return (1);
	return (0);
}

int	ft_str_is_uppercase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(check(str[i])))
			return (0);
		i++;
	}
	return (1);
}
